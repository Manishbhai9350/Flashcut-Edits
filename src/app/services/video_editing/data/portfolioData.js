// Helper function to get unique categories
export const getCategories = (videos) => {
  const categories = ['All', ...new Set(videos.map(video => video.category))];
  return categories;
};

// Helper function to filter videos by category
export const filterVideosByCategory = (videos, category) => {
  if (category === 'All') {
    return videos;
  }
  return videos.filter(video => video.category === category);
};

// Helper function to get video by ID
export const getVideoById = (id) => {
  const allVideos = [...videoPortfolio['short-form'].videos, ...videoPortfolio['long-form'].videos];
  return allVideos.find(video => video.id === parseInt(id));
};

// Helper function to get related videos
export const getRelatedVideos = (currentVideo, limit = 3) => {
  const allVideos = [...videoPortfolio['short-form'].videos, ...videoPortfolio['long-form'].videos];
  const relatedVideos = allVideos
    .filter(video =>
      video.id !== currentVideo.id &&
      (video.category === currentVideo.category || video.platform === currentVideo.platform)
    )
    .slice(0, limit);

  return relatedVideos;
};
